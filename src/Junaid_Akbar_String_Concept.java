public class Junaid_Akbar_String_Concept {


    public static void main(String[] args) {
        String university="Columbia University";


        System.out.println(university.charAt(7));
        System.out.println(university.toUpperCase());
        System.out.println(university.toLowerCase());
        System.out.println(university.length());
        System.out.println(university.substring(5));
        System.out.println(university.replace("Columbia","Oxford"));
        System.out.println(university.concat(" best in the world"));
        System.out.println(university.compareTo(" Stanford"));
        System.out.println(university.indexOf(7,2));
        System.out.println(university.equals("Saint John's"));



    }


}
